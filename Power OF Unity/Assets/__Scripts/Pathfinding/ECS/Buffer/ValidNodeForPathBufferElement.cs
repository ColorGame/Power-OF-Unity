using Unity.Entities;
using Unity.Mathematics;
/// <summary>
/// Элемент буффера -Допустимая сеточная позиция для пути
/// </summary>
[InternalBufferCapacity(20)]//Количество элементов в буфере
public struct ValidNodeForPathBufferElement: IBufferElementData
{
    public int3 gridPosition;

    //публичный статический неявный оператор который принимает сеточную позицию и
    //возвращает новый элемент буфера, установив значение свойства = значению которые мы передаем
    public static implicit operator ValidNodeForPathBufferElement(int3 gridPosition)
    {
        return new ValidNodeForPathBufferElement { gridPosition = gridPosition };
    }
}
